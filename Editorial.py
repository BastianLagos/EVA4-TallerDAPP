class Editorial():
    __idedi= 0
    __nomedi = ""

    def __init__(self):
        pass

    def setIdEdi(self,id):
        self.__idedi = id

    def setNomEdi(self,nomedi):
        self.__nomedi = nomedi
    
    def getIdAut(self):
        return self.__idedi

    def getNomAut(self):
        return self.__nomedi